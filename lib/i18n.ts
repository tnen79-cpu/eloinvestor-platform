function getBucket(namespace: string, lang: SupportedLang) {
  return lang === 'en' ? `${namespace}_en` : namespace;
}

export function i18nText(translations: UiTranslations | undefined, namespace: string, key: string, lang: SupportedLang, fallback = '') {
  const activeBucket = translations?.[namespace] || {};
  const dbValue = activeBucket[key];
  if (dbValue && String(dbValue).trim()) return dbValue;
  const defaults = DEFAULT_TRANSLATIONS[getBucket(namespace, lang)] || DEFAULT_TRANSLATIONS[namespace] || {};
  const value = defaults[key];
  return value || fallback || key;
}

export function flattenDefaultTranslations() {
  const rows: Array<{ namespace: string; translation_key: string; ar: string; en: string; is_active: boolean }> = [];
  const namespaces = Object.keys(DEFAULT_TRANSLATIONS).filter((key) => !key.endsWith('_en'));
  for (const namespace of namespaces) {
    const arBucket = DEFAULT_TRANSLATIONS[namespace] || {};
    const enBucket = DEFAULT_TRANSLATIONS[`${namespace}_en`] || {};
    for (const key of Object.keys({ ...arBucket, ...enBucket })) {
      rows.push({
        namespace,
        translation_key: key,
        ar: arBucket[key] || '',
        en: enBucket[key] || '',
        is_active: true,
      });
    }
  }
  return rows;
}
